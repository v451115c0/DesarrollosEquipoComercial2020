        $(document).ready(function() {
            App.init();
        });

        var contadorsended = 0;
        var contadorsendederror = 0;
        var abiCodeErrors = "";

        function countAbis(){
            var abis = $("#abicodes").val();
            var total = abis.split(',');
            $("#abisAdded").text(total.length);
        }

        function SendMails(){
            var abis = $("#abicodes").val();
            var arrayAbis = abis.split(',');
            for(var x = 0; x < arrayAbis.length; x++){
                ajaxMailSend(arrayAbis[x]);
            }
            abiCodeErrors = "";
            $("#errorAbiList").text(abiCodeErrors);
            contadorsended = 0;
            contadorsendederror = 0;
        }

        function ajaxMailSend(abicode){
            var _token = $("#_token").val();
            var data = {_token: _token, abiCode: abicode};
            $.ajax({
                type: "post",
                url: "/finzsSalMail",
                data: data,
                success: function(response){
                    contadorsended++;
                    $("#mailSended").text(contadorsended);
                },
                error: function(jqXHR, exception) {
                    getErrorMessage(jqXHR, exception);
                    contadorsendederror++;
                    $("#mailSendError").text(contadorsendederror);
                    abiCodeErrors = abicode + ',' + abiCodeErrors;
                    $("#errorAbiList").text(abiCodeErrors);
                }
            });
        }

        function getErrorMessage(jqXHR, exception) {
            var msg = '';
            if (jqXHR.status === 0) {
                msg = 'Not connect.\n Verify Network.';
            } else if (jqXHR.status == 404) {
                msg = 'Requested page not found. [404]';
            } else if (jqXHR.status == 500) {
                msg = 'Internal Server Error [500].';
            } else if (exception === 'parsererror') {
                msg = 'Requested JSON parse failed.';
            } else if (exception === 'timeout') {
                msg = 'Time out error.';
            } else if (exception === 'abort') {
                msg = 'Ajax request aborted.';
            } else {
                msg = 'Uncaught Error.\n' + jqXHR.responseText;
            }
            $('#errormsg').html(msg);
        }
